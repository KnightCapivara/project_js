// Introdução a variaveis e funções.

// para variavel no js: 'const' - é uma constante, ou seja, algo que não va mudar. e 'let' - variavel mutavel. PS: Sempre que puder use 'const'

//  exemplos.:

let pessoa = { firstName: 'Danillo', lastName: 'Acassio'}

const texto = '${pessoa.firstName} => ${pessoa.lastName}';

console.log(texto);

// 'console.log' - para debbugin, se usa na construção do desenvolvimento, para o usuario final não faz menor difernça!